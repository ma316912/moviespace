(function() {
    'use strict';

    angular
        .module('moviespaceApp')
        .controller('MovieController', MovieController);

    MovieController.$inject = ['$scope', '$state', 'Movie'];

    function MovieController ($scope, $state, Movie) {
        var vm = this;
        
        vm.movies = [];

        loadAll();

        function loadAll() {
            Movie.query(function(result) {
                vm.movies = result;
            });
        }
    }
})();
